namespace DBEntity
{
    partial class LetterOfCredit
    {
        private decimal? _distributionAmount = 0;
        public decimal? DistriButionAmount
        {
            get
            {
                if (LCCIRels != null && LCCIRels.Count > 0)
                {
                    _distributionAmount = 0;
                    foreach (var rels in LCCIRels)
                    {
                        if (rels.IsDeleted == false)
                        {
                            _distributionAmount += (rels.AllocationAmount ?? 0);
                        }
                    }
                }

                return _distributionAmount;
            }
            set
            {
                if (_distributionAmount != value)
                {
                    _distributionAmount = value;
                    OnPropertyChanged("DistriButionAmount");
                }
            }
        }

        public bool AutoGenGeneratedCanOperation
        {
            get
            {
                return !IsAutoGenerated;
            }
        }
    }
}
